create table if not exists students (
 student_id bigint generated by default as identity primary key, name text not null unique,
 school_location text not null default '', grade text not null default '', monthly_fee integer not null default 0,
 recital_fee integer not null default 0, enrollment_date date, withdrawal_date date,
 enrollment_status text not null default '在籍' check (enrollment_status in ('在籍','休会','退会')),
 guardian_name text default '', phone text default '', email text default '', notes text default '',
 created_at timestamptz not null default now(), updated_at timestamptz not null default now()
);
create table if not exists charges (
 charge_id bigint generated by default as identity primary key, student_id bigint not null references students(student_id),
 target_month text not null, charge_type text not null, charge_amount integer not null check(charge_amount>=0),
 due_date date, charge_status text not null default '請求中', notes text default '',
 created_at timestamptz not null default now(), updated_at timestamptz not null default now(),
 unique(student_id,target_month,charge_type)
);
create table if not exists payments (
 payment_id bigint generated by default as identity primary key, charge_id bigint not null references charges(charge_id),
 student_id bigint not null references students(student_id), student_name text not null, target_month text not null,
 payment_type text not null, charge_amount integer not null, amount_received integer not null check(amount_received>0),
 received_date date not null default current_date, received_time time not null default localtime,
 payment_method text not null, received_by text not null, stamp_confirmed boolean not null default true,
 stamp_confirmed_at timestamptz, stamp_confirmed_by text, payment_status text not null default '処理完了',
 calendar_event_id text, notes text default '', created_at timestamptz not null default now(), updated_at timestamptz not null default now(),
 cancelled_at timestamptz, cancellation_reason text
);
create table if not exists audit_logs (
 log_id bigint generated by default as identity primary key, action_type text not null, target_table text not null,
 target_id bigint, student_id bigint, action_detail text not null, operator_name text not null,
 action_datetime timestamptz not null default now()
);
create table if not exists allowed_teachers (
 email text primary key, display_name text default '', created_at timestamptz not null default now()
);
create table if not exists calendar_mappings (
 normalized_title text primary key, student_id bigint not null references students(student_id),
 original_title text not null, created_by text not null, created_at timestamptz not null default now(), updated_at timestamptz not null default now()
);
create table if not exists migration_runs (
 source_sha256 text primary key, source_name text not null, imported_at timestamptz not null default now(), detail jsonb not null default '{}'::jsonb
);
create unique index if not exists payments_calendar_event_once
 on payments(calendar_event_id) where calendar_event_id is not null and cancelled_at is null;
